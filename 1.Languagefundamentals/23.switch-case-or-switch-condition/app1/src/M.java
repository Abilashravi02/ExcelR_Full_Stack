class M
{
    public static void main(String[] args) 
    {
        String i = "aaa";
        switch(i)
        {
             
              
            case "abc":
              System.out.println("from case \"abc\"");
              break;
           
            case "bvc":
              System.out.println("from case \"bvc\"");
              break;
            case "cvb":
              System.out.println("from case cvb");
             break;
              default:
              System.out.println("from case default");
             break;
             
        }
         System.out.println("main end");  
    }
}
